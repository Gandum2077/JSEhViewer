function createImage(size, colorName) {
    let hex
    if (colorName[0] !== "#") {
        const result = normalizedHtmlColors.find(n => colorName.toLowerCase() === n.name)
        if (!result) {
            throw new Error("unlisted color name")
        } else {
            hex = result.hex
        }
    } else if (colorName.length === 4) {
        hex = '#' + colorName.toUpperCase().slice(1).split('').map(n => n.repeat(2)).join('')
    } else {
        hex = colorName.toUpperCase()
    }
    const base64 = imageBase64[hex]
    if (!base64) {
        throw new Error("unlisted color")
    }
    const img = $image(base64)
    const output = $imagekit.scaleFill(img, size)
    return output
}

module.exports = createImage
